/**
 * HTML escaping utility — prevents XSS in generated menu HTML.
 * Converts: < > & " ' to their HTML entity equivalents.
 */
export function escapeHtml(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
